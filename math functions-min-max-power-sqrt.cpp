#include <iostream>
#include<cmath>

using namespace std;

int main()
{

    cout <<pow(2,3)<<endl; //square,cube
    cout<<sqrt(36)<<endl; //square root
    cout<<fmax(3,5)<<endl; //max no
    cout<<fmin(8,3)<<endl; //min no
    cout<<round(5.3)<<endl; //to round up values
    cout<<ceil(4.1)<<endl; //convert decimal to next int value
    cout<<floor(5.4)<<endl; //convert decimal to lower int value
    return 0;
}
